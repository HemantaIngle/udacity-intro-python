# Nested List
n_list = ["Hello","How","are",  "you"] # Nested indexing
# Output: Hello
print(n_list[0])
print(n_list[1])
print(n_list[2])
print(n_list[3])

my_list = ['H','E','M','A','N' ,'T'] # define a custom list
# Output: H
print(my_list[0])

# Output: E
print(my_list[1])

# Output: M
print(my_list[2])

# Output: A
print(my_list[3])

# Output: N
print(my_list[4])

# Output: T
print(my_list[5])


# Note-Only integer can be used for indexing
